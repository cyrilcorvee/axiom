package axiom.oauth.oauth2;

import axiom.oauth.AbstractOAuthContext;

public class Oauth2Context extends AbstractOAuthContext {

    private String authRequestUrl;
    private String tokenRequestUrl;
    private String client_id;
    private String response_type;
    private String display;
    private String code;
    private String grant_type;
    private String issued_at;
    private String refresh_token;
    private String instance_url;
    private String signature;
    private String access_token;
    private String id;
    private String error;
    private String error_description;
    private String redirect_uri;
    protected String client_secret;
    //
    protected String authorise_endpoint;
    protected String state;
    protected String scope;


    public Oauth2Context(String host, String redirect_uri) {
        super(host);
        this.redirect_uri = redirect_uri;
    }

    public Oauth2Context(String host, String authorise_endpoint, String redirect_uri) {
        super(host);
        this.authorise_endpoint = authorise_endpoint;
        this.redirect_uri = redirect_uri;
    }

    public String getAuthRequestUrl() {
        return authRequestUrl;
    }

    public void setAuthRequestUrl(String authRequestUrl) {
        this.authRequestUrl = authRequestUrl;
    }

    public String getTokenRequestUrl() {
        return tokenRequestUrl;
    }

    public void setTokenRequestUrl(String tokenRequestUrl) {
        this.tokenRequestUrl = tokenRequestUrl;
    }

    public String getClient_id() {
        return client_id;
    }

    public void setClient_id(String client_id) {
        this.client_id = client_id;
    }

    public String getResponse_type() {
        return response_type;
    }

    public void setResponse_type(String response_type) {
        this.response_type = response_type;
    }

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getGrant_type() {
        return grant_type;
    }

    public void setGrant_type(String grant_type) {
        this.grant_type = grant_type;
    }

    public String getIssued_at() {
        return issued_at;
    }

    public void setIssued_at(String issued_at) {
        this.issued_at = issued_at;
    }

    public String getRefresh_token() {
        return refresh_token;
    }

    public void setRefresh_token(String refresh_token) {
        this.refresh_token = refresh_token;
    }

    public String getInstance_url() {
        return instance_url;
    }

    public void setInstance_url(String instance_url) {
        this.instance_url = instance_url;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

    public String getAccess_token() {
        return access_token;
    }

    public void setAccess_token(String access_token) {
        this.access_token = access_token;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    public String getError_description() {
        return error_description;
    }

    public void setError_description(String error_description) {
        this.error_description = error_description;
    }

    public String getRedirect_uri() {
        return redirect_uri;
    }

    public void setRedirect_uri(String redirect_uri) {
        this.redirect_uri = redirect_uri;
    }

    public String getClient_secret() {
        return client_secret;
    }

    public void setClient_secret(String client_secret) {
        this.client_secret = client_secret;
    }

    public String getAuthorise_endpoint() {
        return authorise_endpoint;
    }

    public void setAuthorise_endpoint(String authorise_endpoint) {
        this.authorise_endpoint = authorise_endpoint;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }
}